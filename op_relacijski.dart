/*RELACIJSKI OPERATORI ==, !=, <, >, <=, >= */
void main(List<String> args) {
  int f = 3;
  int g = 5;

  print('f je $f, g je $g');
  print('f jednako g je ${f == g}');
  print('f je različito od g je ${f != g}');
  print('f je manje od g je ${f < g}');
  print('f je veće od g je ${f > g}');
  print('f je manje ili jednako g je ${f <= g}');
  print('f je veće ili jednako g je ${f >= g}');
}
